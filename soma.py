#Este programa soma, multiplica, subitrai e divide numeros inseridos.

numero1 = 12 #criação de variavel numero1
numero2 = 14 #criação de variavel numero2

soma = numero1 + numero2 #criação de variavel soma
multipli = numero1 * numero2 #criação de variavel multiplicação
sub = numero1 - numero2 #criação de variavel subitração
div = numero1 / numero2 #criação de variavel divisão

print(f"A soma do {numero1} + {numero2} é: {soma}")
print(f"A multiplicação do {numero1} * {numero2} é: {multipli}")
print(f"A subitração do {numero1} - {numero2} é: {sub}")
print(f"A divisão do {numero1} / {numero2} é: {div}")
print("A soma de {} + {} é: {}" .format(numero1, numero2, soma))